def converter_str_para_int():
    numero : int = int ("2")
    print("numero: " + str (numero))

def converter_str_para_float():
    valor: float = float ("2293.112")
    print("Valor: " + str(valor))

def converter_str_para_boolean():
    ativo: bool = bool("true")
    print("Ativo: " + str(ativo))
    